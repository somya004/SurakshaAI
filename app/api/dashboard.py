@router.get("/worker-safety")
async def get_worker_safety_dashboard(
    plant_id: str | None = None,
    zone_id: str | None = None,
    db: AsyncSession = Depends(get_db),
):
    """
    Return each worker's latest location and PPE safety status.

    Important rules:
    - No PPE data means "not_verified", not "non_compliant".
    - PPE compliance percentage is calculated only from verified PPE records.
    - Missing exposure data is returned as "unknown".
    """

    # ---------------------------------------------------------
    # 1. Find the timestamp/order column for WorkerLocation
    # ---------------------------------------------------------

    location_order_column = None

    for column_name in [
        "timestamp",
        "recorded_at",
        "updated_at",
        "created_at",
        "entry_time",
        "id",
    ]:
        if hasattr(WorkerLocation, column_name):
            location_order_column = getattr(
                WorkerLocation,
                column_name,
            )
            break

    # ---------------------------------------------------------
    # 2. Fetch worker location records
    # ---------------------------------------------------------

    location_query = select(WorkerLocation)

    if (
        plant_id is not None
        and hasattr(WorkerLocation, "plant_id")
    ):
        location_query = location_query.where(
            WorkerLocation.plant_id == plant_id
        )

    if (
        zone_id is not None
        and hasattr(WorkerLocation, "zone_id")
    ):
        location_query = location_query.where(
            WorkerLocation.zone_id == zone_id
        )

    if location_order_column is not None:
        location_query = location_query.order_by(
            desc(location_order_column)
        )

    location_result = await db.execute(
        location_query
    )

    location_records = (
        location_result.scalars().all()
    )

    # ---------------------------------------------------------
    # 3. Keep only the latest location for each worker
    # ---------------------------------------------------------

    latest_locations = {}

    for location in location_records:

        worker_id = getattr(
            location,
            "worker_id",
            None,
        )

        if worker_id is None:
            continue

        if worker_id not in latest_locations:
            latest_locations[worker_id] = location

    # ---------------------------------------------------------
    # 4. Find timestamp/order column for WorkerPPEStatus
    # ---------------------------------------------------------

    ppe_order_column = None

    for column_name in [
        "timestamp",
        "recorded_at",
        "updated_at",
        "created_at",
        "inspection_time",
        "id",
    ]:
        if hasattr(WorkerPPEStatus, column_name):
            ppe_order_column = getattr(
                WorkerPPEStatus,
                column_name,
            )
            break

    # ---------------------------------------------------------
    # 5. Dashboard counters
    # ---------------------------------------------------------

    workers_data = []

    total_workers_inside = 0

    compliant_workers = 0
    non_compliant_workers = 0
    unverified_ppe_workers = 0

    workers_at_risk = 0

    # ---------------------------------------------------------
    # 6. Process each worker
    # ---------------------------------------------------------

    for worker_id, location in latest_locations.items():

        # -----------------------------------------------------
        # Determine whether the worker is inside the plant
        # -----------------------------------------------------

        if hasattr(location, "is_inside"):
            is_inside = bool(
                location.is_inside
            )

        elif hasattr(location, "inside_plant"):
            is_inside = bool(
                location.inside_plant
            )

        elif hasattr(location, "status"):
            is_inside = (
                str(location.status)
                .strip()
                .lower()
                in {
                    "inside",
                    "active",
                    "present",
                    "entered",
                }
            )

        else:
            # A current location record means the worker
            # is assumed to be inside and tracked.
            is_inside = True

        if not is_inside:
            continue

        total_workers_inside += 1

        # -----------------------------------------------------
        # Fetch latest PPE record
        # -----------------------------------------------------

        ppe_query = select(
            WorkerPPEStatus
        ).where(
            WorkerPPEStatus.worker_id == worker_id
        )

        if ppe_order_column is not None:
            ppe_query = ppe_query.order_by(
                desc(ppe_order_column)
            )

        ppe_query = ppe_query.limit(1)

        ppe_result = await db.execute(
            ppe_query
        )

        latest_ppe = (
            ppe_result.scalar_one_or_none()
        )

        # -----------------------------------------------------
        # PPE status handling
        # -----------------------------------------------------

        ppe_data_available = (
            latest_ppe is not None
        )

        ppe_compliant = None
        ppe_status = "not_verified"
        missing_ppe = []

        if latest_ppe is not None:

            # Try to read one supported compliance field.
            if hasattr(
                latest_ppe,
                "is_compliant",
            ):
                raw_ppe_compliance = (
                    latest_ppe.is_compliant
                )

                if raw_ppe_compliance is not None:
                    ppe_compliant = bool(
                        raw_ppe_compliance
                    )

            elif hasattr(
                latest_ppe,
                "ppe_compliant",
            ):
                raw_ppe_compliance = (
                    latest_ppe.ppe_compliant
                )

                if raw_ppe_compliance is not None:
                    ppe_compliant = bool(
                        raw_ppe_compliance
                    )

            elif hasattr(
                latest_ppe,
                "compliant",
            ):
                raw_ppe_compliance = (
                    latest_ppe.compliant
                )

                if raw_ppe_compliance is not None:
                    ppe_compliant = bool(
                        raw_ppe_compliance
                    )

            elif hasattr(
                latest_ppe,
                "status",
            ):
                ppe_status_value = str(
                    latest_ppe.status
                ).strip().lower()

                if ppe_status_value in {
                    "compliant",
                    "safe",
                    "complete",
                }:
                    ppe_compliant = True

                elif ppe_status_value in {
                    "non_compliant",
                    "non-compliant",
                    "unsafe",
                    "incomplete",
                }:
                    ppe_compliant = False

            # Read missing PPE list when available.
            if hasattr(
                latest_ppe,
                "missing_ppe",
            ):
                missing_ppe = (
                    latest_ppe.missing_ppe
                    or []
                )

            elif hasattr(
                latest_ppe,
                "missing_items",
            ):
                missing_ppe = (
                    latest_ppe.missing_items
                    or []
                )

            # Normalize missing PPE into a Python list.
            if isinstance(
                missing_ppe,
                str,
            ):
                missing_ppe = [
                    item.strip()
                    for item in missing_ppe.split(",")
                    if item.strip()
                ]

            elif not isinstance(
                missing_ppe,
                list,
            ):
                try:
                    missing_ppe = list(
                        missing_ppe
                    )
                except TypeError:
                    missing_ppe = []

            # If missing PPE is listed, the worker cannot
            # be considered compliant.
            if len(missing_ppe) > 0:
                ppe_compliant = False

            if ppe_compliant is True:
                ppe_status = "compliant"
                compliant_workers += 1

            elif ppe_compliant is False:
                ppe_status = "non_compliant"
                non_compliant_workers += 1

            else:
                ppe_status = "not_verified"
                unverified_ppe_workers += 1

        else:
            # No PPE record exists.
            unverified_ppe_workers += 1

        # -----------------------------------------------------
        # Read worker exposure level
        # -----------------------------------------------------

        exposure_level = getattr(
            location,
            "exposure_level",
            None,
        )

        if exposure_level is None:
            exposure_level = "unknown"

        normalized_exposure = (
            str(exposure_level)
            .strip()
            .lower()
        )

        # -----------------------------------------------------
        # Determine worker risk
        # -----------------------------------------------------

        ppe_risk = (
            ppe_compliant is False
        )

        environmental_risk = (
            normalized_exposure
            in {
                "high",
                "critical",
            }
        )

        worker_at_risk = (
            ppe_risk
            or environmental_risk
        )

        if worker_at_risk:
            workers_at_risk += 1

        # -----------------------------------------------------
        # Fetch worker details
        # -----------------------------------------------------

        worker_query = (
            select(Worker)
            .where(
                Worker.worker_id == worker_id
            )
            .limit(1)
        )

        worker_result = await db.execute(
            worker_query
        )

        worker = (
            worker_result.scalar_one_or_none()
        )

        worker_name = None

        if worker is not None:
            worker_name = (
                getattr(
                    worker,
                    "name",
                    None,
                )
                or getattr(
                    worker,
                    "full_name",
                    None,
                )
                or getattr(
                    worker,
                    "worker_name",
                    None,
                )
            )

        # -----------------------------------------------------
        # Find latest location timestamp
        # -----------------------------------------------------

        location_timestamp = None

        for field_name in [
            "timestamp",
            "recorded_at",
            "updated_at",
            "created_at",
            "entry_time",
        ]:
            if hasattr(
                location,
                field_name,
            ):
                location_timestamp = getattr(
                    location,
                    field_name,
                )

                if location_timestamp is not None:
                    break

        # -----------------------------------------------------
        # Find latest PPE timestamp
        # -----------------------------------------------------

        ppe_timestamp = None

        if latest_ppe is not None:
            for field_name in [
                "timestamp",
                "recorded_at",
                "updated_at",
                "created_at",
                "inspection_time",
            ]:
                if hasattr(
                    latest_ppe,
                    field_name,
                ):
                    ppe_timestamp = getattr(
                        latest_ppe,
                        field_name,
                    )

                    if ppe_timestamp is not None:
                        break

        # -----------------------------------------------------
        # Add worker to response
        # -----------------------------------------------------

        workers_data.append(
            {
                "worker_id": worker_id,
                "worker_name": worker_name,
                "plant_id": getattr(
                    location,
                    "plant_id",
                    plant_id,
                ),
                "zone_id": getattr(
                    location,
                    "zone_id",
                    zone_id,
                ),
                "is_inside": is_inside,
                "ppe_data_available": (
                    ppe_data_available
                ),
                "ppe_status": ppe_status,
                "ppe_compliant": (
                    ppe_compliant
                ),
                "missing_ppe": missing_ppe,
                "exposure_level": (
                    exposure_level
                ),
                "worker_at_risk": (
                    worker_at_risk
                ),
                "last_location_update": (
                    location_timestamp
                ),
                "last_ppe_update": (
                    ppe_timestamp
                ),
            }
        )

    # ---------------------------------------------------------
    # 7. Calculate PPE compliance percentage
    # ---------------------------------------------------------

    verified_ppe_workers = (
        compliant_workers
        + non_compliant_workers
    )

    if verified_ppe_workers > 0:
        ppe_compliance_percentage = round(
            (
                compliant_workers
                / verified_ppe_workers
            )
            * 100,
            2,
        )
    else:
        ppe_compliance_percentage = None

    # ---------------------------------------------------------
    # 8. Return dashboard response
    # ---------------------------------------------------------

    return {
        "plant_id": plant_id,
        "zone_id": zone_id,
        "total_workers_inside": (
            total_workers_inside
        ),
        "ppe_compliant_workers": (
            compliant_workers
        ),
        "ppe_non_compliant_workers": (
            non_compliant_workers
        ),
        "ppe_unverified_workers": (
            unverified_ppe_workers
        ),
        "workers_at_risk": (
            workers_at_risk
        ),
        "ppe_compliance_percentage": (
            ppe_compliance_percentage
        ),
        "workers": workers_data,
    }
    
@router.get("/machine-health")
async def get_machine_health_dashboard(
    plant_id: str | None = Query(
        default=None,
        description="Filter machines by plant ID",
    ),
    zone_id: str | None = Query(
        default=None,
        description="Filter machines by zone ID",
    ),
    db: AsyncSession = Depends(get_db),
):
    """
    Returns the latest health condition of machines.

    The endpoint combines:

    - machine-anomaly information
    - compound-risk events
    - equipment risk severity
    - maintenance status
    - overdue maintenance

    No duplicate machine-health table is created.
    """

    # -------------------------------------------------
    # 1. LOAD RISK EVENTS
    # -------------------------------------------------

    risk_query = select(RiskEvent)

    if plant_id is not None:
        risk_query = risk_query.where(
            RiskEvent.plant_id == plant_id
        )

    if zone_id is not None:
        risk_query = risk_query.where(
            RiskEvent.zone_id == zone_id
        )

    risk_result = await db.execute(
        risk_query
    )

    risk_events = list(
        risk_result.scalars().all()
    )

    # -------------------------------------------------
    # 2. LOAD MAINTENANCE RECORDS
    # -------------------------------------------------

    maintenance_result = await db.execute(
        select(MaintenanceOrder)
    )

    maintenance_records = list(
        maintenance_result.scalars().all()
    )

    # Maintenance filters are applied safely in Python
    # because model field names may vary.
    filtered_maintenance = []

    for maintenance in maintenance_records:
        maintenance_plant_id = getattr(
            maintenance,
            "plant_id",
            None,
        )

        maintenance_zone_id = getattr(
            maintenance,
            "zone_id",
            None,
        )

        if (
            plant_id is not None
            and maintenance_plant_id != plant_id
        ):
            continue

        if (
            zone_id is not None
            and maintenance_zone_id != zone_id
        ):
            continue

        filtered_maintenance.append(
            maintenance
        )

    # -------------------------------------------------
    # 3. COLLECT ALL MACHINE IDS
    # -------------------------------------------------

    machine_ids: set[str] = set()

    for event in risk_events:
        equipment_id = getattr(
            event,
            "equipment_id",
            None,
        )

        if equipment_id:
            machine_ids.add(
                str(equipment_id)
            )

    for maintenance in filtered_maintenance:
        equipment_id = (
            getattr(
                maintenance,
                "equipment_id",
                None,
            )
            or getattr(
                maintenance,
                "machine_id",
                None,
            )
            or getattr(
                maintenance,
                "asset_id",
                None,
            )
        )

        if equipment_id:
            machine_ids.add(
                str(equipment_id)
            )

    # -------------------------------------------------
    # 4. BUILD MACHINE HEALTH INFORMATION
    # -------------------------------------------------

    machines = []

    healthy_machines = 0
    warning_machines = 0
    critical_machines = 0
    machines_under_maintenance = 0
    maintenance_due = 0

    for machine_id in sorted(machine_ids):

        machine_risk_events = [
            event
            for event in risk_events
            if str(
                getattr(
                    event,
                    "equipment_id",
                    "",
                )
            )
            == machine_id
        ]

        machine_maintenance_records = []

        for maintenance in filtered_maintenance:
            maintenance_machine_id = (
                getattr(
                    maintenance,
                    "equipment_id",
                    None,
                )
                or getattr(
                    maintenance,
                    "machine_id",
                    None,
                )
                or getattr(
                    maintenance,
                    "asset_id",
                    None,
                )
            )

            if (
                maintenance_machine_id is not None
                and str(maintenance_machine_id)
                == machine_id
            ):
                machine_maintenance_records.append(
                    maintenance
                )

        # ---------------------------------------------
        # 4.1 LATEST RISK EVENT
        # ---------------------------------------------

        machine_risk_events.sort(
            key=lambda event: (
                get_event_timestamp(event)
                or datetime.min
            ),
            reverse=True,
        )

        latest_risk_event = (
            machine_risk_events[0]
            if machine_risk_events
            else None
        )

        # ---------------------------------------------
        # 4.2 LATEST MAINTENANCE RECORD
        # ---------------------------------------------

        machine_maintenance_records.sort(
            key=lambda maintenance: (
                get_maintenance_timestamp(
                    maintenance
                )
                or datetime.min
            ),
            reverse=True,
        )

        latest_maintenance = (
            machine_maintenance_records[0]
            if machine_maintenance_records
            else None
        )

        # ---------------------------------------------
        # 4.3 MACHINE LOCATION
        # ---------------------------------------------

        machine_plant_id = None
        machine_zone_id = None

        if latest_risk_event is not None:
            machine_plant_id = getattr(
                latest_risk_event,
                "plant_id",
                None,
            )

            machine_zone_id = getattr(
                latest_risk_event,
                "zone_id",
                None,
            )

        if latest_maintenance is not None:
            machine_plant_id = (
                machine_plant_id
                or getattr(
                    latest_maintenance,
                    "plant_id",
                    None,
                )
            )

            machine_zone_id = (
                machine_zone_id
                or getattr(
                    latest_maintenance,
                    "zone_id",
                    None,
                )
            )

        # ---------------------------------------------
        # 4.4 ANOMALY CONDITION
        # ---------------------------------------------

        anomaly_detected = False
        anomaly_score = None

        if latest_risk_event is not None:
            anomaly_detected = (
                contains_machine_anomaly(
                    latest_risk_event
                )
            )

            anomaly_score = (
                get_machine_anomaly_score(
                    latest_risk_event
                )
            )

        # ---------------------------------------------
        # 4.5 RISK CONDITION
        # ---------------------------------------------

        latest_risk_level = ""

        if latest_risk_event is not None:
            latest_risk_level = normalize_value(
                getattr(
                    latest_risk_event,
                    "risk_level",
                    None,
                )
            )

        latest_risk_score = None

        if latest_risk_event is not None:
            raw_risk_score = getattr(
                latest_risk_event,
                "risk_score",
                None,
            )

            if raw_risk_score is not None:
                try:
                    latest_risk_score = round(
                        float(raw_risk_score),
                        2,
                    )
                except (TypeError, ValueError):
                    latest_risk_score = None

        # ---------------------------------------------
        # 4.6 MAINTENANCE CONDITION
        # ---------------------------------------------

        under_maintenance = any(
            is_active_maintenance(
                maintenance
            )
            for maintenance
            in machine_maintenance_records
        )

        overdue_maintenance = any(
            is_maintenance_overdue(
                maintenance
            )
            for maintenance
            in machine_maintenance_records
        )

        maintenance_status = "not_scheduled"
        maintenance_due_date = None

        if latest_maintenance is not None:
            maintenance_status = (
                normalize_value(
                    getattr(
                        latest_maintenance,
                        "status",
                        None,
                    )
                )
                or "unknown"
            )

            maintenance_due_date = (
                get_maintenance_due_date(
                    latest_maintenance
                )
            )

        if overdue_maintenance:
            maintenance_status = "overdue"

        elif under_maintenance:
            maintenance_status = (
                "under_maintenance"
            )

        # ---------------------------------------------
        # 4.7 FINAL MACHINE HEALTH
        # ---------------------------------------------

        if under_maintenance:
            health_status = "under_maintenance"

        elif (
            latest_risk_level == "critical"
            or (
                latest_risk_score is not None
                and latest_risk_score >= 81
            )
            or (
                anomaly_score is not None
                and anomaly_score >= 80
            )
        ):
            health_status = "critical"

        elif (
            anomaly_detected
            or overdue_maintenance
            or latest_risk_level
            in {
                "warning",
                "high",
            }
            or (
                latest_risk_score is not None
                and latest_risk_score >= 41
            )
        ):
            health_status = "warning"

        else:
            health_status = "healthy"

        # ---------------------------------------------
        # 4.8 UPDATE TOTAL COUNTS
        # ---------------------------------------------

        if health_status == "healthy":
            healthy_machines += 1

        elif health_status == "warning":
            warning_machines += 1

        elif health_status == "critical":
            critical_machines += 1

        elif health_status == "under_maintenance":
            machines_under_maintenance += 1

        if overdue_maintenance:
            maintenance_due += 1

        # ---------------------------------------------
        # 4.9 MACHINE RESPONSE
        # ---------------------------------------------

        machines.append(
            {
                "machine_id": machine_id,
                "plant_id": machine_plant_id,
                "zone_id": machine_zone_id,
                "health_status": health_status,
                "anomaly_detected": (
                    anomaly_detected
                ),
                "anomaly_score": anomaly_score,
                "latest_risk_score": (
                    latest_risk_score
                ),
                "latest_risk_level": (
                    latest_risk_level or None
                ),
                "maintenance_status": (
                    maintenance_status
                ),
                "under_maintenance": (
                    under_maintenance
                ),
                "maintenance_overdue": (
                    overdue_maintenance
                ),
                "maintenance_due_date": (
                    maintenance_due_date
                ),
                "last_risk_update": (
                    get_event_timestamp(
                        latest_risk_event
                    )
                    if latest_risk_event
                    else None
                ),
                "last_maintenance_update": (
                    get_maintenance_timestamp(
                        latest_maintenance
                    )
                    if latest_maintenance
                    else None
                ),
            }
        )

    # Critical machines appear first in the frontend.
    health_priority = {
        "critical": 1,
        "warning": 2,
        "under_maintenance": 3,
        "healthy": 4,
    }

    machines.sort(
        key=lambda machine: (
            health_priority.get(
                machine["health_status"],
                5,
            ),
            machine["machine_id"],
        )
    )

    return {
        "plant_id": plant_id,
        "zone_id": zone_id,
        "total_machines": len(machines),
        "healthy_machines": healthy_machines,
        "warning_machines": warning_machines,
        "critical_machines": critical_machines,
        "machines_under_maintenance": (
            machines_under_maintenance
        ),
        "maintenance_due": maintenance_due,
        "machines": machines,
    }
    
@router.get("/emergency-response")
async def get_emergency_response_dashboard(
    plant_id: str | None = None,
    zone_id: str | None = None,
    incident_id: int | None = None,
    db: AsyncSession = Depends(get_db),
):
    """
    Returns emergency-response actions generated for incidents.

    Workflow:
    Risk Event
    -> Incident
    -> Alert
    -> Response Action
    -> Emergency Response Dashboard
    """

    # ---------------------------------------------------------
    # 1. LOAD RESPONSE ACTIONS
    # ---------------------------------------------------------

    query = select(ResponseAction)

    if plant_id is not None:
        query = query.where(
            ResponseAction.plant_id == plant_id
        )

    if zone_id is not None:
        query = query.where(
            ResponseAction.zone_id == zone_id
        )

    if incident_id is not None:
        query = query.where(
            ResponseAction.incident_id == incident_id
        )

    query = query.order_by(
        desc(ResponseAction.created_at)
    )

    result = await db.execute(query)

    response_actions = list(
        result.scalars().all()
    )

    # ---------------------------------------------------------
    # 2. DASHBOARD COUNTERS
    # ---------------------------------------------------------

    total_actions = len(response_actions)

    pending_actions = 0
    in_progress_actions = 0
    completed_actions = 0
    verified_actions = 0
    overdue_actions = 0
    critical_actions = 0
    mandatory_pending_actions = 0

    active_incident_ids = set()
    responder_names = set()
    assigned_roles = set()

    action_data = []

    now = datetime.now(timezone.utc)

    total_response_seconds = 0.0
    response_time_count = 0

    total_completion_seconds = 0.0
    completion_time_count = 0

    # ---------------------------------------------------------
    # 3. PROCESS EACH RESPONSE ACTION
    # ---------------------------------------------------------

    for action in response_actions:

        status = (
            action.status.value
            if hasattr(action.status, "value")
            else str(action.status)
        )

        status = status.strip().lower()

        priority = (
            action.priority.value
            if hasattr(action.priority, "value")
            else str(action.priority)
        )

        priority = priority.strip().lower()

        action_type = (
            action.action_type.value
            if hasattr(action.action_type, "value")
            else str(action.action_type)
        )

        action_type = action_type.strip().lower()

        # -----------------------------------------------------
        # STATUS COUNTS
        # -----------------------------------------------------

        if status in {
            "pending",
            "open",
            "assigned",
        }:
            pending_actions += 1

        elif status in {
            "in_progress",
            "in progress",
            "ongoing",
            "started",
        }:
            in_progress_actions += 1

        elif status in {
            "completed",
            "resolved",
            "closed",
        }:
            completed_actions += 1

        elif status in {
            "verified",
        }:
            completed_actions += 1
            verified_actions += 1

        if action.verified_at is not None:
            verified_actions += 1

        # Avoid double-counting verified actions.
        if (
            status == "verified"
            and action.verified_at is not None
        ):
            verified_actions -= 1

        # -----------------------------------------------------
        # CRITICAL ACTIONS
        # -----------------------------------------------------

        if priority in {
            "critical",
            "emergency",
            "urgent",
        }:
            critical_actions += 1

        # -----------------------------------------------------
        # OVERDUE ACTIONS
        # -----------------------------------------------------

        is_completed = status in {
            "completed",
            "resolved",
            "closed",
            "verified",
        }

        action_due_at = action.due_at

        if action_due_at is not None:
            if action_due_at.tzinfo is None:
                action_due_at = (
                    action_due_at.replace(
                        tzinfo=timezone.utc
                    )
                )

        is_overdue = (
            not is_completed
            and action_due_at is not None
            and action_due_at < now
        )

        if is_overdue:
            overdue_actions += 1

        # -----------------------------------------------------
        # MANDATORY PENDING ACTIONS
        # -----------------------------------------------------

        if (
            action.mandatory
            and not is_completed
        ):
            mandatory_pending_actions += 1

        # -----------------------------------------------------
        # ACTIVE INCIDENTS
        # -----------------------------------------------------

        if not is_completed:
            active_incident_ids.add(
                action.incident_id
            )

        # -----------------------------------------------------
        # RESPONDERS AND ROLES
        # -----------------------------------------------------

        if action.assigned_to:
            responder_names.add(
                action.assigned_to
            )

        if action.assigned_role:
            assigned_roles.add(
                action.assigned_role
            )

        # -----------------------------------------------------
        # RESPONSE TIME
        # created_at -> started_at
        # -----------------------------------------------------

        response_time_minutes = None

        if (
            action.created_at is not None
            and action.started_at is not None
        ):
            created_at = action.created_at
            started_at = action.started_at

            if created_at.tzinfo is None:
                created_at = created_at.replace(
                    tzinfo=timezone.utc
                )

            if started_at.tzinfo is None:
                started_at = started_at.replace(
                    tzinfo=timezone.utc
                )

            response_seconds = (
                started_at - created_at
            ).total_seconds()

            if response_seconds >= 0:
                total_response_seconds += (
                    response_seconds
                )

                response_time_count += 1

                response_time_minutes = round(
                    response_seconds / 60,
                    2,
                )

        # -----------------------------------------------------
        # COMPLETION TIME
        # started_at -> completed_at
        # -----------------------------------------------------

