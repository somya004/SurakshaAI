def normalize_value(value) -> str:
    """
    Converts strings or enum values into a lowercase string.
    """

    if value is None:
        return ""

    if hasattr(value, "value"):
        value = value.value

    return str(value).strip().lower()


def contains_machine_anomaly(risk_event: RiskEvent) -> bool:
    """
    Checks whether a risk event contains machine-anomaly
    information generated by the compound-risk workflow.
    """

    predicted_event = normalize_value(
        getattr(
            risk_event,
            "predicted_event",
            None,
        )
    )

    explanation = normalize_value(
        getattr(
            risk_event,
            "explanation",
            None,
        )
    )

    contributing_factors = normalize_value(
        getattr(
            risk_event,
            "contributing_factors",
            None,
        )
    )

    anomaly_keywords = [
        "machine anomaly",
        "abnormal machine",
        "machine behaviour",
        "machine behavior",
        "equipment anomaly",
        "abnormal equipment",
        "anomaly score",
    ]

    combined_text = " ".join(
        [
            predicted_event,
            explanation,
            contributing_factors,
        ]
    )

    return any(
        keyword in combined_text
        for keyword in anomaly_keywords
    )


def get_machine_anomaly_score(
    risk_event: RiskEvent,
) -> float | None:
    """
    Extracts the machine-anomaly score when the RiskEvent
    model already stores it as a separate field.

    Returns None when it is only present inside explanation
    text.
    """

    possible_fields = [
        "anomaly_score",
        "machine_anomaly_score",
        "machine_score",
    ]

    for field_name in possible_fields:
        value = getattr(
            risk_event,
            field_name,
            None,
        )

        if value is not None:
            try:
                return round(
                    float(value),
                    2,
                )
            except (TypeError, ValueError):
                return None

    return None


